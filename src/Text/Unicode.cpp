Optional<Text::Unicode::Block> Text::Unicode::GetBlock(UInt32 code)
{
	OSInt i = 0;
	OSInt j = (sizeof(blocks) / sizeof(blocks[0])) - 1;
	OSInt k;
	Block *blk;
	while (i <= j)
	{
		k = (i + j) >> 1;
		blk = &blocks[k];
		if (code < blk->beginCode)
		{
			j = k - 1;
		}
		else if (code > blk->endCode)
		{
			i = k + 1;
		}
		else
		{
			return blk;
		}
	}
	return nullptr;
}
