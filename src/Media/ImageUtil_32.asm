;void ImageUtil_SwapRGB(UInt8 *imgPtr, OSInt pixelCnt, OSInt bpp)
;0 esi
;4 retAddr
;8 imgPtr
;12 pixelCnt
;16 bpp
	align 16
_ImageUtil_SwapRGB:
ImageUtil_SwapRGB:
	push esi
	mov esi,dword [esp+8] ;imgData
	mov ecx,dword [esp+12] ;pixelCnt
	
	mov eax,dword [esp+16] ;bpp
	cmp eax,48
	jz srgb48start
	cmp eax,64
	jz srgb64start
	cmp eax,24
	jz srgb24start
	cmp eax,32
	jz srgb32start
	jmp srgbexit

	align 16
srgb48start:
	mov ax,word [esi]
	mov dx,word [esi+4]
	mov word [esi],dx
	mov word [esi+4],ax
	add esi,6
	dec ecx
	jnz srgb48start
	jmp srgbexit
	
	align 16
srgb64start:
	mov ax,word [esi]
	mov dx,word [esi+4]
	mov word [esi],dx
	mov word [esi+4],ax
	add esi,8
	dec ecx
	jnz srgb64start
	jmp srgbexit
	
	align 16
srgb24start:
	mov al,byte [esi]
	mov dl,byte [esi+2]
	mov byte [esi],dl
	mov byte [esi+2],al
	add esi,3
	dec ecx
	jnz srgb24start
	jmp srgbexit

	align 16
srgb32start:
	mov al,byte [esi]
	mov dl,byte [esi+2]
	mov byte [esi],dl
	mov byte [esi+2],al
	add esi,4
	dec ecx
	jnz srgb32start
	jmp srgbexit

	align 16
srgbexit:
	pop esi
	ret
	
