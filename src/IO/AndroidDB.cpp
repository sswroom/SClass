const IO::AndroidDB::AndroidInfo *IO::AndroidDB::GetAndroidInfo(Text::CString androidId)
{
	OSInt i = 0;
	OSInt j = (sizeof(androids) / sizeof(androids[0])) - 1;
	OSInt k;
	OSInt l;

	while (i <= j)
	{
		k = (i + j) >> 1;
		l = Text::StrCompareFastC(androidId.v, androidId.leng, androids[k].androidId, androids[k].androidIdLen);
		if (l > 0)
		{
			i = k + 1;
		}
		else if (l < 0)
		{
			j = k - 1;
		}
		else
		{
			return &androids[k];
		}
	}
	return 0;
}
